-- modules (D010/D011: baked artifacts are committed so users never need the
-- font package or a build step). Run from the repo root inside the devshell:
--
--   nix develop -c lua tools/bake_spleen.lua
--
-- Output: engine/cm/assets/font_<gw>x<gh>.lua — a grid atlas of ASCII
-- 32..126 as a 1bpp hex string (row-major, MSB-first), decoded to an RGBA
-- texture at runtime by cm.text.

